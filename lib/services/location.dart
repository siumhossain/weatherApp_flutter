class Location{
  double longitude;
  double latitude;
  Future<void> getCurrentLocation() async{
    try{
      Position position = await Geolocator.getCurrentPosition(desiredAccuracy: LocationAccuracy.medium);
      longitude = position.longitude;
      latitude = position.latitude;
    }catch(e){
      print(e);
    }

  }

}


